/**
 * Получает матрицу для умножения в ОНБ из её сжатого представления.
 *
 * @param compress_mulp сжатое представление матрицы
 * @param M матрица для умножения в ОНБ размера 2 * m - 1
 */
static void decompress_matrix(const WordArray *compress_mulp, uint16_t *mulp)
{
    int n = (int) compress_mulp->len;
    size_t mlen = (int_bit_len(compress_mulp) + 8) / 9;
    int offset = 0;
    int i = n;
    int j = (int)mlen;

    while (--i >= 0) {
        while ((offset < (WORD_BIT_LENGTH - 8)) && (--j >= 0)) {
            mulp[j] = (compress_mulp->buf[n - 1 - i] >> offset) & 0x1ff;
            offset += 9;
        }

        if (--j >= 0) {
            if (offset != WORD_BIT_LENGTH) {
                mulp[j] = (compress_mulp->buf[n - 1 - i] >> offset) & 0x1ff;
            } else {
                mulp[j] = 0;
            }
            offset -= (WORD_BIT_LENGTH - 9);
            if (i > 0) {
                mulp[j] |= (compress_mulp->buf[n - 1 - (i - 1)] << (9 - offset)) & 0x1ff;
            }
        }
    }
}

/**
 * Вычисляет остаток от деления a mod m, при условии, что a < 2m.
 *
 * @param a целое число < 2m
 * @param m модуль
 *
 * @return a mod m
 */
static int simple_mod(int a, int m)
{
    return a < m ? a : a - m;
}

/**
 * Выполняет умножение в поле GF(2^m) в ОНБ.
 *
 * @param x первый множитель
 * @param y второй множитель
 * @param mulp матрица для умножения в ОНБ
 * @param m степень расширения поля GF(2^m)
 * @param r массив для x * y
 */
static void multiply_onb(const WordArray *x, const WordArray *y, const uint16_t *mulp, int m, WordArray *r)
{
    int i, j;
    wa_zero(r);

    /* XXX: очень медленная фукнция */
    for (j = 0; j < m; j++) {
        word_t bit = 0;
        for (i = 0; i < m - 1; i++) {
            word_t t1 = int_get_bit(y, simple_mod(mulp[2 * i] + j + 1, m));
            word_t t2 = int_get_bit(y, simple_mod(mulp[2 * i + 1] + j + 1, m));
            word_t t3 = int_get_bit(x, simple_mod(i + j + 1, m));
            bit ^= (t1 ^ t2) & t3;
        }

        bit ^= int_get_bit(y, simple_mod(mulp[2 * m - 2] + j + 1, m)) & int_get_bit(x, simple_mod(m - 1 + j + 1, m));
        /* XOR j бит у r */
        r->buf[j >> WORD_BIT_LEN_SHIFT] ^= bit << (j & WORD_BIT_LEN_MASK);
    }
}

